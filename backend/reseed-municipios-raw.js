async function seed() {
    const URI = 'mongodb+srv://adminhotel:hotel2026@cluster0.zsiq9ye.mongodb.net/HotelDB?retryWrites=true&w=majority';
    try {
        await mongoose.connect(URI);
        console.log('Connected.');

        await Municipio.deleteMany({});
        console.log('Collection cleared.');

        const lines = rawData.split('\n').map(l => l.trim()).filter(l => l.length > 0);
        const docs = lines.map(line => {
            const parts = line.split('-');
            return {
                nombre: line,
                departamento: parts[0] || 'DESCONOCIDO',
                visualizar: false
            };
        });

        console.log(`Inserting ${docs.length} municipios...`);
        await Municipio.insertMany(docs);
        console.log('Reseeded successfully.');
        
        process.exit(0);
    } catch (err) {
        console.error(err);
        process.exit(1);
    }
}

seed();
